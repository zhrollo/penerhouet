	Um mit der Buchung der Unterkunft fortzufahren, zahlt der Mieter eine Anzahlung in Höhe von 30% des Mietpreises.<br/>

	Die Zahlung der Anzahlung kann erfolgen durch :<br/>
	<ul>
		<li>Überweisung auf das Konto des Vermieters</li>
	</ul>
</p>				

<h3>Artikel 8 - Zahlung des Preises</h3>
<b>Der Restbetrag (einschließlich 70% der Miete für die gesamte Mietdauer und die Kurtaxe) muss vom Mieter spätestens beim Betreten der Unterkunft bezahlt werden. </B> <br/>
Der Taker wird diese Zahlung per Scheck oder in bar vornehmen.<br/>
<br/>
Zu Beginn der Vermietung gibt der Vermieter dem Mieter die Schlüssel und die Anweisungen bezüglich der Unterkunft; Ein Inventar wird auch zwischen den beiden Parteien erstellt.<br/>

<h3>Article 9 – Dépôt de garantie</h3>

Spätestens bei Betreten der Räumlichkeiten <b> wird der Vermieter dem Vermieter einen Betrag von 500,00 Euro als Kaution hinterlegen </b> um den Schaden und / oder Schäden an Wohnraum und Mobiliar zu decken Objekte, die die Wohnung des Mieters verkleiden, sowie der Verlust von Schlüsseln oder Gegenständen.<br/>
<br/>
Die Kaution wird dem Mieter innerhalb von maximal einem Monat nach der Abreise zurückerstattet, abzüglich eventueller Schäden und / oder Schäden an der Unterkunft und den Möbeln und Gegenständen, die vom Mieter verursacht wurden wie der Verlust von Schlüsseln oder Objekten.<br/>
<br/>
Die Anzahlung kann durch die Lieferung eines Kautionsschecks erfolgen, der im Auftrag des Vermieters unterschrieben ist und von diesem gemäß dem Inventar und dem Inventar zurückgesandt wird, das der Rückgabe der Schlüssel entspricht.<br/>

<h3>Artikel 10 - Übertragung und Untervermietung</h3>

Dieser Mietvertrag wird zu Gunsten des einzigen Mieters abgeschlossen, der zu Beginn des Vertrags identifiziert wurde.<br/>
<br/>
Jede Abtretung dieses Mietvertrags, jeglicher Untermietvertrags insgesamt oder teilweise, jegliche Bereitstellung - sogar kostenlos - ist strengstens untersagt. Der Mieter darf die Räumlichkeiten nicht kostenfrei und / oder leihweise einer Person außerhalb seines Hauses überlassen.<br/>


<h3>Artikel 11 - Inventar</h3>

<b>Ein Inventar und ein Inventar der dem Mieter zur Verfügung gestellten Möbel werden dem Mieter beim Eintritt in die Wohnung gegeben.</b><br/>
Wenn das Inventar und das Inventar nicht gleichzeitig vom Vermieter oder seinem Vertreter und dem Mieter erstellt und unterzeichnet werden (Inventar und widersprüchliche Inventare), Inventar des Inventar und Inventar des Vermieters nur und an den Mieter bei seiner Einreise in die Unterkunft geliefert wird bestraft werden durch den Mieter innerhalb von 48 Stunden nach der Einreise in die Wohnung. Wenn der Mieter innerhalb dieser Frist von 48 Stunden keine Einwände erhebt, gelten das Inventar und das Inventar, das vom Vermieter gemacht und dem Mieter bei seiner Einfahrt in die Räumlichkeiten mitgeteilt wurde, als vom Mieter vorbehaltlos akzeptiert.<br/>
<br/>
Ein Inventar und ein Inventar werden von den Parteien am Ende des Mietvertrags erstellt, wobei jeder eine unterzeichnete Kopie beibehält.<br/>
<br/>
Wenn am Ende des Mietverhältnisses kein Inventar und / oder Inventar vorhanden ist oder wenn der Mieter am Ende des Mietverhältnisses das Inventar der Einrichtungsgegenstände und / oder des Inventars erstellt, ist die Uneinigkeit der Vermieter innerhalb von 48 Stunden nach dem Ende der Miete wird in guten Zustand und / oder vollständige Bestandsaufnahme wert sein.


<h3>Artikel 12 - Erklärung des Vermieters</h3>

Der Vermieter erklärt, dass er der Eigentümer der Wohnung ist und während der in Absatz 3 genannten Frist frei darüber verfügt und vollen Gebrauch davon macht.<br/>
<br/>
Der Vermieter wird dies durch die Vorlage einer EEF-Quittung, einer Grundsteuer-Bekanntmachung oder eines anderen offiziellen Dokuments begründen.<br/>

<h3>Artikel 13 - Pflichten des Mieters</h3>

- Der Mieter wird die gemietete Unterkunft und die Möbel und die Ausrüstung nach dem Ziel, das ihm durch den Mietvertrag gegeben wurde, friedlich nutzen und für die Schäden und Verluste, die während der Laufzeit des Vertrags in den Räumen, die er ausschließlich genießt, auftreten können.<br/>
<br/>
- Der Mieter behält die gemietete Unterkunft und wird es am Ende des Vertrages in einem guten Zustand der Sauberkeit und Mietreparaturen machen. Wenn Gegenstände im Inventar beschädigt oder beschädigt sind, kann der Vermieter ihren Ersatzwert geltend machen.<br/>
<br/>
- Es muss Lärm vermieden werden, der die Nachbarn belästigt, insbesondere solche, die von Radio, Fernsehen und anderen Geräten ausgestrahlt werden.<br/>
<br/>
- Bei Diebstahl und Plünderungen in den Mieträumen kann der Mieter keinen Rückgriff gegen den Vermieter ausüben.<br/>
<br/>
- <b>Er respektiert die Anzahl von maximal 7 Personen, die an den Orten schlafen können</b>, gemäß der Beschreibung, die ihm gegeben wurde.<br/>
<br/>
- Der Mieter darf dem Besuch der Räumlichkeiten nicht widersprechen, wenn der Vermieter oder sein Vertreter dies verlangen.<br/>
<br/>
- In Ermangelung einer Restitution der Unterkunft in einwandfreiem Zustand verpflichtet sich der Mieter, auf eigene Kosten die Reinigung zu übernehmen, die der Vermieter durchführen muss.<br/>

<h3>Artikel 14 - Stornierung</h3>

Die Unterzeichnung des Vertrages bindet beide Parteien unwiderruflich. Eine Kündigung ist ohne schriftliche Zustimmung der Parteien nicht möglich. Verlässt der Mieter die Miete, bleibt er für die volle Miete haftbar.<br/>

<h3>Artikel 15 - Versicherung</h3>

Der Mieter hat eine Multi-Risiko-Versicherung abgeschlossen, um sich vor den Vermietungsrisiken (Wasserschaden, Feuer ...) zu schützen. Der Mangel an Versicherung, im Falle einer Katastrophe, wird zu Schäden führen.<br/>
Der Vermieter verpflichtet sich, das Mietobjekt im Namen des Mieters gegen Mietschäden zu versichern. Letzterer ist verpflichtet, ihm innerhalb von 24 Stunden alle Vorkommnisse in den Wohnungen, deren Abhängigkeiten oder Zubehör zu melden.<br/>
<br/>
<b>Der Mieter muss daher prüfen, ob sein Hauptwohnungsvertrag eine Resortverlängerung (Ferienvermietung) vorsieht. Im gegenteiligen Fall muss er bei seiner Versicherungsgesellschaft eingreifen und ihn um die Verlängerung der Garantie bitten oder einen bestimmten Vertrag unter der Klausel "Urlaub" abschließen. Ein Versicherungsschein wird am Eingang des Hauses oder bei Nichterfüllung einer ehrenwörtlichen Erklärung verlangt.</b>
<br/>

<h3>Artikel 16 - Beendigung des Rechts</h3>

Im Falle einer Verletzung einer der vertraglichen Verpflichtungen durch den Leasingnehmer wird dieser Mietvertrag automatisch gekündigt. Diese Kündigung wird nach einer Verspätung von 48 Stunden wirksam, nachdem eine einfache Ladung per eingeschriebenem Brief oder Brief von Hand fehlgeschlagen ist.<br/>

<h3>Artikel 17 - Wahl des Wohnsitzes</h3>

Für die Ausführung dieses Vertrages wählen der Vermieter und der Mieter den Wohnsitz in ihren jeweiligen Wohnungen. Im Streitfall ist jedoch ausschließlich das Gericht am Sitz des Vermieters zuständig. Dieser Vertrag und seine Folgen unterliegen französischem Recht.<br/>

