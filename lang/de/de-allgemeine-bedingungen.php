<h3>Artikel 1 - Allgemeine Bestimmungen</h3>

Keine Änderung, Löschung oder Überlastung wird bei der Erstellung des Mietvertrages oder in diesen Geschäftsbedingungen ohne Zustimmung beider Parteien akzeptiert.

<h3>Artikel 2 - Zweck des Mietvertrages</h3>
<p>				
	Die Parteien erklären, dass dieser Mietvertrag nicht für gemietete Räumlichkeiten zur Nutzung als Hauptwohnsitz oder gemischt genutzter Betrieb und Hauptwohnraum bestimmt ist.<br/>
	<br/>
	Sie stimmen daher zu, dass ihre jeweiligen Rechte und Pflichten den Bestimmungen dieses Vertrags unterliegen, und zwar durch das Dekret vom 28. Dezember 1976, geändert durch die Bestimmungen des Zivilgesetzbuches.<br/>
	<br/>
	Die unter diesen Vertrag fallenden Räumlichkeiten werden saisonal möbliert vermietet.<br/>
</p>

<h3>Artikel 3 - Zweck des Mietvertrages</h3>
