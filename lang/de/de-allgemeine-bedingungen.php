<h3>Artikel 4 - Dauer des Aufenthalts</h3>
<p>				
Der Vermieter vermietet die Unterkunft an den Mieter für einen Zeitraum zwischen den beiden Parteien (in der Regel von Samstag bis Samstag für einen Zeitraum von 7, 14 oder 21 Tagen)<br/>
<br/>
Unter keinen Umständen darf der Mieter ein Recht auf Aufrechterhaltung der Räumlichkeiten am Ende des Aufenthalts geltend machen.<br/>
</p>


<h3>Artikel 5 - Ankunfts- und Abfahrtszeiten</h3>
<p>
	<b>Die Mietwoche beginnt am Samstag um 15.30 Uhr und endet am darauf folgenden Samstag um 10.30 Uhr entsprechend der in den Wochen festgelegten Dauer.</b><br/>
	Im Falle einer Änderung der Ankunftszeit muss der Mieter die Person, die ihn erhält, so schnell wie möglich telefonisch informieren und ihm seine ungefähre Ankunftszeit mitteilen. <br/>
Check-out ist um 10:30 Uhr vom Eigentümer oder seinem Vertreter.<br/>
	Der Mieter erklärt sich ausdrücklich damit einverstanden, die Unterkunft spätestens am Samstag um 10.30 Uhr freizugeben und die Schlüssel an den Vermieter zurückzugeben.<br/>
</p>

<h3>Artikel 6 - Zweck des Mietvertrages</h3>
<p>				
	Die Parteien verpflichten sich, einen bestimmten Betrag in Abhängigkeit von der für die gesamte Dauer des Mietverhältnisses gewählten Dauer zu mieten.<br/>
	<br/>
	Zu diesem Betrag kommt die <b> Kurtaxe </b> hinzu, die 1.10 &euro; / Tag / (Erwachsener oder Kind über 13 Jahre) vom Nehmer fällig ist.<br/>
	<br/>
	Die oben genannte Miete beinhaltet für die Dauer des Mietverhältnisses die Zahlung der Mietgebühren und der verfügbaren Materialien, die nachfolgend aufgeführt sind :<br/>
	- Stadtwasser / Pumpwasser<br/>
	- Flaschen Gas<br/>
	- Heizung<br/>
	- Strom<br/>
	<br/>
	Der Vermieter übergibt dem Mieter eine Quittung für die geleistete Zahlung.<br/>
</p>

<h3>Artikel 7 - Buchungen und Anzahlungen</h3>
