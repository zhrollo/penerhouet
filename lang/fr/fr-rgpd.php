<h3>Introduction</h3>

Nous vous présentons dans cette page les principes avec lesquels penerhouet.com recueille différents types de données sur ses utilisateurs, ainsi que nos engagements concernant la politique de sécurité et de protection des données afin de respecter au maximum votre vie privée.

	
<h3>Responsable du traitement</h3>

Tous les traitement des informations personnelles communiquées à penerhouet.com sont effectués sous la responsabilité de Samuel Besnard (<a href="mailto:contact@penerhouet.com">contact@penerhouet.com</a>).

	
<h3>Demandes de modification de vos données personnelles</h3>

Pour toute demande d’accès, d’obtention, de rectification ou d'effacement de vos données personnelles, nous vous invitons à nous contacter (<a href="mailto:contact@penerhouet.com">contact@penerhouet.com</a>).

	
<h3>Comment recueillons-nous des données à votre sujet ?</h3>

La collecte de vos données personnelles est réalisée lors des saisies de formulaire que nous proposons sur le site.<br/><br/>

Lorsque vous visitez le site web www.penerhouet.com, que vous en chargez des pages, vous nous laissez différents types de données de navigation anonymes, telles que votre adresse IP et votre historique de navigation. Le fonctionnement des services est basé sur l'utilisation de cookies.

	
<h3>Quelles données recueillons-nous ?</h3>

<p>
