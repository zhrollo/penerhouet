<h3>Comment gérons-nous vos informations ?</h3>

<p>
	Veuillez noter que nous agissons sous l'obligation du secret lorsque nous traitons les données vous concernant.<br/><br/>

	Nous maintenons la confidentialité des données vous concernant et veillons à ce qu'elles ne soient utilisées qu'à des fins prédéfinies.<br/><br/>

	Vos données sont traitées dans le but de vous informer et informer les utilisateurs de penerhouet.com afin de rendre compte des expériences des vacanciers.<br/><br/>

	Nous utilisons également les données pour rédiger les documents contractuels de réservation (contrat de location, facturation, ...),  gérer en interne notre planning de réservation, gérer notre comptabilité, déclarer auprès de la Mairie de Locmariaquer le règlement de la taxe de séjour.<br/><br/>

	Nous nous efforçons de nous assurer que les données des utilisateur sont à jour et correctes.<br/><br/>

	Nous supprimons les données obsolètes et inutiles lorsque cela est possible.<br/><br/>

	Nous protégeons toutes les données vous concernant par le biais de droits d'accès personnels basés sur des tâches et empêchons l'accès des tiers aux données.
<p/>
	
<h3>Où envoyons-nous vos données ?</h3>

<p>Nous ne soumettons pas vos données hormis votre nom et les dates de séjour à la Mairie de Locmariaquer lors du règlement de la taxe de séjour.</p>

	


 
