	</ul>
</p>				

<h3>Article 8 – Règlement du prix</h3>
<b>Le solde (comprenant les 70% du loyer de la durée complète et la taxe de séjour), est à régler par le Preneur au plus tard lors de l’entrée dans le logement.</b><br/>
Le Preneur effectuera ce versement par chèque ou espèce.<br/>
<br/>
Lors du début de la location, le Bailleur remettra au Preneur les clefs et les instructions relatives au logement ; un état des lieux sera également dressé entre les deux parties.<br/>

<h3>Article 9 – Dépôt de garantie</h3>

Au plus tard lors de l’entrée dans les lieux, <b>le Preneur remettra au Bailleur un montant de 500.00 euros à titre de dépôt de garantie</b> destiné à couvrir les dommages et/ou dégradations du logement et du mobilier et objets garnissant le logement causés par le Preneur, ainsi que les pertes de clefs ou d'objets.<br/>
<br/>
Le dépôt de garantie sera restitué au Preneur dans un délai maximum d’un mois après son départ, déduction faite le cas échéant des sommes couvrant les dommages et/ou dégradations du logement et du mobilier et objets garnissant le logement causés par le Preneur, ainsi que les pertes de clefs ou d'objets.<br/>
<br/>
Le dépôt de garantie pourra être constitué par la remise d'un chèque de caution signé à l'ordre du Bailleur que ce dernier restituera sous réserve d'inventaire et d'état des lieux conforme lors de la restitution des clefs.<br/>

<h3>Article 10 – Cession et sous-location</h3>

Le présent contrat de location est conclu intuitu personae au profit du seul preneur identifié en tête du contrat.<br/>
<br/>
Toute cession du présent bail, toute sous-location totale ou partielle, toute mise à disposition -même gratuite-, sont rigoureusement interdites. Le Preneur ne pourra laisser la disposition des lieux, même gratuitement et/ou par prêt, à une personne étrangère à son foyer.<br/>


<h3>Article 11 – Etat des lieux et inventaires</h3>

<b>Un état des lieux et un inventaire du mobilier mis à disposition du Preneur sont remis au preneur lors de l’entrée dans le logement.</b><br/>
Si l'état des lieux et l'inventaire ne sont pas établis et signés par le Bailleur, ou son représentant, et le Preneur simultanément (état des lieux et inventaires contradictoires), l'état des lieux et l'inventaire réalisé par le Bailleur seul et remis au Preneur lors de son entrée dans le logement seront contestables par le Preneur dans un délai de 48 heures suivant l'entrée dans le logement. A défaut de contestation par le Preneur dans ce délai de 48 heures, l'état des lieux et l'inventaire réalisés par le Bailleur et communiqués au Preneur à son entrée dans les lieux seront réputés acceptés sans réserve par le Preneur.<br/>
<br/>
Un état des lieux et un inventaire seront établis par les Parties à la fin de la location, chacune en conservant un exemplaire signé.<br/>
<br/>
A défaut d'état des lieux et/ ou d'inventaire à la fin de la location ou si le Preneur établit seul l'état des lieux et/ ou l'inventaire à la fin de la location, l'absence de contestation par le Bailleur dans les 48 heures suivant la fin de la location vaudra restitution des lieux en bon état et/ou inventaire complet.


<h3>Article 12 – Déclaration du bailleur</h3>

Le Bailleur déclare être propriétaire du logement et en avoir la libre disposition et la pleine jouissance durant la période définie au paragraphe 3.<br/>
<br/>
Le Bailleur en justifiera par la production d’une quittance EDF, d'un avis de Taxe Foncière ou de tout autre document officiel.<br/>

<h3>Article 13 – Obligations du preneur</h3>

- Le Preneur usera paisiblement du logement loué et du mobilier et équipements suivant la destination qui leur a été donnée par le bail et répondra des dégradations et pertes qui pourraient survenir pendant la durée du contrat dans les locaux dont il a la jouissance exclusive.<br/>
<br/>
- Le Preneur entretiendra le logement loué et le rendra en bon état de propreté et de réparations locatives en fin de contrat. Si des objets figurant à l’inventaire sont brisés ou détériorés, le Bailleur pourra réclamer leur valeur de remplacement.<br/>
<br/>
- Il devra éviter tout bruit de nature à gêner les voisins, notamment ceux émis par les appareils de radio, télévision et autres.<br/>
<br/>
- Le Preneur ne pourra exercer aucun recours contre le Bailleur en cas de vol et déprédations dans les lieux loués.<br/>
<br/>
- <b>Il respectera le nombre de 7 personnes maximum pouvant dormir dans les lieux</b>, conformément au descriptif qui lui a été remis.<br/>
<br/>
- Le preneur ne pourra s’opposer à la visite des locaux si le Bailleur ou son mandataire en font la demande.<br/>
<br/>
- A défaut de restitution du logement en parfait état de propreté, le Preneur s'engage à prendre à ses frais le nettoyage que le Bailleur sera contraint de réaliser.<br/>
<h3>Article 14 – Annulation</h3>

La signature du contrat engage les deux parties de manière irrévocable. Aucune résiliation n’est possible sauf accord écrit des parties. Si le Preneur renonce à la location, il reste redevable de la totalité du loyer.<br/>

<h3>Article 15 – Assurances</h3>

Le Preneur a contracté une assurance multirisque afin de se prémunir contre les risques locatifs (dégâts des eaux, incendies…). Le défaut d'assurance, en cas de sinistre, donnera lieu à des dommages et intérêts.<br/>
Le bailleur s'engage à assurer le logement contre les risques locatifs pour le compte du locataire, ce dernier ayant l'obligation de lui signaler, dans les 24 h, tout sinistre survenu dans le logement, ses dépendances ou accessoires.<br/>
<br/>
<b>Le preneur doit donc vérifier si son contrat d'habitation principale prévoit l'extension villégiature (location de vacances). Dans l'hypothèse contraire, il doit intervenir auprès de sa compagnie d'assurances et lui réclamer l'extension de la garantie ou bien souscrire un contrat particulier, au titre de clause "villégiature". Une attestation d'assurances lui sera réclamée à l'entrée dans la maison ou à défaut une déclaration sur l'honneur.</b>
<br/>

<h3>Article 16 – Résiliation de plein droit</h3>

En cas de manquement par le Preneur à l’une des obligations contractuelles, le présent bail sera résilié de plein droit. Cette résiliation prendra effet après un délai de 48 heures après une simple sommation par lettre recommandée ou lettre remise en main propre restée infructueuse.<br/>

<h3>Article 17 – Election de domicile</h3>

Pour l’exécution des présentes, le Bailleur et le Preneur font élection de domicile dans leurs domiciles respectifs. Toutefois, en cas de litige, le tribunal du domicile du Bailleur sera seul compétent. Le présent contrat et ses suites sont soumis à la loi française.<br/>

