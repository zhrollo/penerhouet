<h3>Article 4 – Durée du séjour</h3>
<p>				
Le Bailleur loue au Preneur le logement pour une période convenue entre les deux parties (généralement du samedi au samedi pour une durée de 7, 14 ou 21 jours).<br/>
<br/>
Le preneur ne pourra en aucune circonstance se prévaloir d’un quelconque droit au maintien dans les lieux à l’issue du séjour.<br/>
</p>


<h3>Article 5 – Horaires d’arrivée et de départ</h3>
<p>
	<b>La semaine de location débute le samedi à 15h30 heures et se termine un des samedi suivants à 10h30 suivant la durée définie en nombre de semaines.</b><br/>
	En cas de changement d’horaire d’arrivée, le locataire devra en informer par téléphone la personne qui l’accueille dès que possible, et lui indiquera son heure approximative d’arrivée.<br/>
	Les formalités de départ s’effectuent à 10h30, par le propriétaire ou son représentant.<br/>
	Le Preneur s'engage expressément à avoir intégralement libéré le logement le samedi à 10h30 au plus tard, et à remettre au Bailleur les clefs.<br/>
</p>

<h3>Article 6 – Objet du contrat de location</h3>
<p>				
	Les Parties conviennent d'un loyer d'un certain montant suivant la période choisie pour l’intégralité de la durée de la location.<br/>
	<br/>
	A ce montant s'ajoute la <b>taxe de séjour</b> est de 1.10 euros / jour / (adulte ou enfant de plus de 13 ans) qui est due par le preneur.<br/>
	<br/>
	Le loyer ci-dessus comprend, pour toute la durée de la location, le paiement des charges locatives et des fournitures disponibles rappelées ci-après :<br/>
	- Eau de ville / Eau de pompage<br/>
	- Gaz bouteilles<br/>
	- Chauffage<br/>
	- Electricité<br/>
	<br/>
	Le Bailleur remettra au Preneur une quittance pour tout versement effectué.<br/>
</p>

<h3>Article 7 – Réservation et acomptes</h3>
	<p>				
	Afin de procéder à la réservation du logement, le Preneur s'acquitte d'un <b>acompte correspondant à 30 % du loyer</b> de la durée complète.<br/>

	Le paiement de l'acompte peut s'effectuer par :<br/>
	<ul>
		<!-- <li>paiement en ligne</li> -->
		<li>chèque à l'ordre du Bailleur</li>
